# Para iniciar el interprete de ruby usamos ´irb´ en la consola
# Tipos de dato enterio y flotante
# Asignar un enterio
x = 5

# Asignar un flotante
y = 10.9

# Operadores
x + y   # suna
x - y   # resta
x / y   # division
x * y   # multiplicacion
x ** x  # potenciacion

# Algunos metodos
x.class     # Regresa la clase del objeto
x.methods   # Regresa los metodos que soporta el objeto
x.odd?      # Regresa true o false
x.even?     # Regresa true o false
